def body():  # 000000

    t.color('black')
    t.fillcolor('#20B49D')
    t.begin_fill()
    t.pensize(2)
    t.penup()
    t.home()
    # 后腿到脚丫
    fleg()
    # 肚子线
    t.seth(3)
    t.pensize(2)
    t.fd(130)
    # 前脚
    rleg()
    # 嘴巴
    zuiba()
    t.seth(90)
    t.fd(85)
    # 三根呆毛
    t.seth(70)
    t.circle(-60, 45)
    t.circle(-60, -45)
    t.seth(100)
    t.circle(-60, 60)
    t.circle(-60, -60)
    t.seth(130)
    t.circle(-60, 70)
    t.circle(-60, -70)

    #
    t.seth(174)
    t.circle(2200, 11)
    t.circle(1100, 1)
    t.circle(5, 90)
    t.goto(fir)
    print('heading:', t.heading())
    t.end_fill()


